package fr.iutvalence.java.projets.towerdefense;

/**
* Enumération contenant les variables de décor
*/
public enum Decor
{
	/**
	 * Correspond au décor terre
	 */
	TERRE,

	/**
	 * Correspond au décor herbe
	 */
	HERBE,

	/**
	 * Correspond au décor route
	 */
	ROUTE,

	/**
	 * Correspond au décor mur
	 */
	MUR,
	
	/**
	 * Correspond au décor de la tower
	 */
	TOWER,
	
	/**
	 * Correspond au décor d'une unité
	 */
	UNITE;
}
